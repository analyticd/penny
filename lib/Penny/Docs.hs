{-|

= About Penny

Penny is a double-entry accounting system that is designed for
programmers, especially programmers that are comfortable with Haskell
or are willing to learn.  Financial data generally is input through
plain-text files, and through programs that manipulate these
plain-text files.  You generate reports by writing programs, typically
in a REPL.  The programs aren't hard to write; using Penny resembles
using a command-line program.

All documentation is here in Haddock, so it is easily accessible in
your Web browser.

